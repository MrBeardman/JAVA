public class wrapperClass {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // WRAPPER CLASSES
		// provides a way to use a primitive datay types as reference data tyoes
		// referecne data types contain useful methods
		//can be used with collections (ex.ArrayList)
		//
		//Primitive        // Wrapper
		//---------        //---------
		// boolean        Boolean
		// char           Character
                //int              Integer
                // double          Double
		
        // autoboxing = the automatic conversion that the Java compiler makes between the primitive data types and their coresponding wrapper class
		// unboxing = the reverse of autoboxing. Automatic conversion of wrapper class to primitive
		
		Boolean a =true;
		Character b = '@';
		Integer c = 123;
		Double d = 3.14;
		String e = "John";
    }
    
}
